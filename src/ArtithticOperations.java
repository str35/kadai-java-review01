
public class ArtithticOperations {

    public static void main(String[] args) {
        // 変数の宣言・初期化
        int num1=100;
        int num2=10;

        int result=num1+num2;
        System.out.println("num1 + num2 = "+result);

        result=num1-num2;
        System.out.println("num1 - num2 = "+result);

        result=num1*num2;
        System.out.println("num1 * num2 = "+result);

        result=num1/num2;
        System.out.println("num1 / num2 = "+result);

        result=num1%num2;
        System.out.println("num1 % num2 = "+result);
    }

}
